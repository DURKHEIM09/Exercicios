package Operadores.Desafio;

public class Ex01 {
	public static void main(String[] args) {
	
		 
		int A = 6 * (3 + 2);
	    int AA = (int) Math.pow(A, 2);
	    
	    int B = 3 * 2;
	    int BB = AA / B;
	    
	    int C = (1-5) * (2-7) / 2;
	    int CC = (int) Math.pow(C, 2);
		
	    int D = BB - CC;
	    int DD = (int) Math.pow(D, 3);
		
		int E = 10;
		int EE = (int) Math.pow(E, 3);
		
		int F = DD / EE;
		System.out.println(F);
		
		
		
		
		
		
		
	}
}

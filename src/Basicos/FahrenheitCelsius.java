package Basicos;

public class FahrenheitCelsius {
	
	
	public static void main(String[] args) {
	final double a = 5.0 /9;
	final double ajuste = 32;
		double Farenheit = 86;
		double celsius = (Farenheit - ajuste ) * a;
		
		
		System.out.println("Conversao de Farenheit para celsius = " + celsius );
	
	
	
	
	}			
}


